// DECLARACION DE FUNCIONES
// Tres formas de declarar funciones

function otraFuncion(){
    console.log("Hola soy la primera funcion");
}
otraFuncion(); //aca estamos llamando la funcion

let miFuncion = function(){
    console.log("Hola soy la segunda funcion");
}
miFuncion(); //aca estamos llamando la funcion

let arrowFuncion = () => {
    console.log("Hola soy la tercera funcion");
}
arrowFuncion(); //aca estamos llamando la funcion